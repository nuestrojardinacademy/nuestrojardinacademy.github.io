<p>
    <h4>FULL AND PART TIME SPANISH PRESCHOOL TEACHER (NE Portland)</h4>
    Open until filled
</p>
<p>
    Nuestro Jardín Academy, a brand new Spanish immersion preschool in Portland, Oregon, is seeking full and part time Spanish-speaking preschool teachers to start up in late spring/beginning of summer 2016. Our preschool provides a high-quality, research-based program that fosters the acquisition of children's first and second language with a focus on developing the whole child. We believe in making this a unique language and cultural immersion experience for children, so we are looking for the perfect candidates to join our team. Our goal is for teachers to work in a fun, supportive, and collaborative environment. Teachers are one of the best assets to our program, so providing high-quality professional development in Spanish will help you become the best teacher you can be.
</p>
<p>
    <strong>Job Description:<br>
    ESSENTIAL FUNCTIONS:<br></strong>
    <ul>
        <li>Responsible for the overall supervision, planning, and management of the Spanish immersion preschool class:</li>
        <li>Create and manage a warm and challenging learning environment for students, incorporating appropriate activities which meet the needs of the group as well as those of individual children and families.</li>
        <li>Provide and maintain a safe environment.</li>
        <li>Be able to read, speak, and write with native-like fluency and proficiency in Spanish.</li>
    </ul>
</p>
<p>
    <strong>RESPONSIBILITIES:</strong><br>
    <ul>
        <li>Be available to substitute for other classes as needed.</li>
        <li>Participate in professional development activities and complete continuing education requirements.</li>
        <li>Adhere to personnel policies as outlined in the Employee Handbook.</li>
        <li>Be responsible and oversee the accomplishment of routine tasks with regard to the health, safety, and well-being of children, and maintain the program in accordance with the requirements of the Oregon Child Care Division.</li>
        <li>Assist in the maintenance of a safe, healthy, and orderly classroom environment.</li>
        <li>Assist in designing and maintaining a warm, nurturing, enriched, and challenging learning environment.</li>
        <li>Assist in creating, revising, and implementing lesson plans based on our Spanish immersion program’s goals and objectives.</li>
    </ul>
</p>

