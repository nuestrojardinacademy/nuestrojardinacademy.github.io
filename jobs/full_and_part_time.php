<p>
    <strong>MINIMUM QUALIFICATIONS:</strong><br>
    <ul>
        <li>Applicant must be bilingual AND be a native Spanish speaker or have native-like fluency/proficiency</li>
        <li>Qualify under one of the following below:</li>
        <ul>
            <li>AA Degree in early childhood education, elementary education, child development, or special education OR</li>
            <li>At least two years of experience working with young children and families in a daycare/ preschool program. OR</li>
            <li>Completion of 20 semester credits or 30 quarter credits of training at a college or university in Early childhood education, child development, elementary education, or special education AND at least one year of experience working with young children and families in a daycare/preschool program.</li>
        </ul>
        <li>Enrollment in Oregon Central Background Registry (can be done right after accepting employment)</li>
        <li>The following are also required for employment and must be obtained within 90 days after accepting the position.</li>
        <ul>
            <li>Current First Aid/CPR card</li>
            <li>Food Handlers Card</li>
            <li>Valid Oregon Driver’s License</li>
        </ul>
    </ul>
